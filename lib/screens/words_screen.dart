  String _buildPdfContent() {
    final buf = StringBuffer();
    buf.writeln('申论规范词');
    buf.writeln('');
    // 按分类组织
    for (final cat in _categories) {
      final words = _wordsForCategory(cat);
      if (words.isEmpty) continue;
      buf.writeln('═══════ $cat ═══════');
      buf.writeln('');
      for (final w in words) {
        final word = (w['word'] as String?) ?? '';
        final explanation = (w['explanation'] as String?) ?? '';
        final usage = (w['usage'] as String?) ?? '';
        buf.writeln('【$word】');
        buf.writeln('释义：$explanation');
        if (usage.isNotEmpty) buf.writeln('例句：$usage');
        buf.writeln('');
      }
    }
    return buf.toString();
  }

  Future<void> _downloadPdf() async {
    try {
      final content = _buildPdfContent();
      final path = await ExportService.exportToPdf('申论规范词', content);
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(content: Text('规范词 PDF 已下载'), duration: Duration(seconds: 2)),
        );
      }
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('下载失败: $e'), duration: const Duration(seconds: 3)),
        );
      }
    }
  }

