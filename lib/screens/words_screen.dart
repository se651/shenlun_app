  Future<void> _load() async {
    // 始终加载备用词（作为兜底 + 补充）
    final fallbackWords = _fallbackWords.map((w) => Map<String, dynamic>.from(w)).toList();
    List<Map<String, dynamic>> dbWords = [];
    try {
      final cats = await _db.getWordCategories();
      if (cats.isNotEmpty) {
        dbWords = await _db.getWords();
      }
    } catch (_) {}

    // 合并：DB 词 + 备用词，按 word+category 去重（DB 优先）
    final seen = <String>{};
    for (final w in dbWords) {
      seen.add('${w['word']}|${w['category']}');
    }
    final merged = <Map<String, dynamic>>[...dbWords];
    for (final w in fallbackWords) {
      final key = '${w['word']}|${w['category']}';
      if (!seen.contains(key)) {
        merged.add(w);
        seen.add(key);
      }
    }

    // 汇总所有分类
    final cats = <String>{};
    for (final w in merged) { cats.add((w['category'] as String?) ?? '其他'); }
    final catList = cats.toList();
    _assignColorsAndIcons(catList);

    if (mounted) setState(() {
      _categories = catList;
      _words = merged;
      _loading = false;
      _usingFallback = dbWords.isEmpty;
    });
  }

  void _useFallback() {
    final cats = <String>{};
    for (final w in _fallbackWords) { cats.add(w['category'] as String); }
    final catList = cats.toList();
    _assignColorsAndIcons(catList);
    if (mounted) setState(() {
      _categories = catList;
      _words = _fallbackWords.map((w) => Map<String, dynamic>.from(w)).toList();
      _loading = false;
      _usingFallback = true;
    });
  }

  void _assignColorsAndIcons(List<String> catList) {
    int fallbackIdx = 0;
    for (final cat in catList) {
      final meta = _categoryMeta[cat];
      if (meta != null) {
        _catColors[cat] = meta['color'] as Color;
        _catIcons[cat] = meta['icon'] as IconData;
      } else {
        _catColors[cat] = _colorList[fallbackIdx % _colorList.length];
        _catIcons[cat] = Icons.folder_rounded;
        fallbackIdx++;
      }
    }
  }

  List<Map<String, dynamic>> _wordsForCategory(String cat) {
    return _words.where((w) => (w['category'] as String?) == cat).toList();
  }

